fn main() {
    // addition
    let sum = 5 + 10;
    println!("The sum is {sum}");
    // subtraction
    let difference = 95.5 - 4.3;
    println!("The difference is {difference}");
    //multiplication
    let product = 7 * 19;
    println!("The product is {product}");
    // division
    let quotient = 56.7 / 32.2;
    let truncated = -5 / 3;
    println!("The quotient is {quotient} and the truncated quotient is {truncated}");
    // remainder
    let remainder = 43 % 5;
    println!("The remainder is {remainder}");
}
